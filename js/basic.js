function isNumber(n) {
  return !isNaN(parseFloat(n)) && isFinite(n);
}

function checkIfExists(item) {
  if (typeof item === 'undefined' || item === null) {
    return false;
  } else {
    return true;
  }
}

function checkIfContains(obj,item) {
  if (checkIfExists(item)) {
    if (Object.hasOwn(obj, item)) {
      return true;
    } else {
      return false;
    }
  } else {
    return false;
  }
}

