function getI18NValue(index) {
  return Object.keys(I18N_VALUES)[index];
}

let CURRENT_I18N = getI18NValue(0);

function createOption(value,inner) {
  let option = document.createElement('option');
  option.value = value;
  option.innerHTML = inner;
  return option;
}

function createSelect(object_values,label,select_id,selectedIndex,callback) {
  let array = Object.keys(object_values);
  let select = document.getElementById(select_id);
  select.innerHTML = '';

  array.forEach((item, index) => {
    let option = createOption(item,object_values[item][label]);
    select.appendChild(option);

    if (index == (array.length - 1)) {
      select.selectedIndex = selectedIndex;

      callback();
    }
  });
}

function createSelectToForm(first_value,object_values,label,select_id,callback) {
  let selectedIndex = document.getElementById(select_id).selectedIndex;
  let array_object_values = Object.keys(object_values);
  let new_object_values = {};
  new_object_values['all'] = { label: getInternationalization(first_value) };
  array_object_values.forEach((item, index) => {
    new_object_values[item] = { label: getInternationalization(object_values[item][label]) };

    if (index == (array_object_values.length - 1)) {
      createSelect(new_object_values,'label',select_id,selectedIndex,callback);
    }
  });
}
