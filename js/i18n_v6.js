function getInternationalizationTerm(i18n,text) {
  if (i18n == 'en-US') {
    return text;
  } else if (checkIfContains(I18N[i18n],text)) {
    return I18N[i18n][text];
  } else {
    return text;
  }
}

function getInternationalization(text) {
  let i18n = CURRENT_I18N;

  if (checkIfContains(I18N_SCREEN_TERMS[i18n],text)) {
    return I18N_SCREEN_TERMS[i18n][text];
  } else {
    if (i18n == 'en-US') {
      return text;
    } else {
      return getInternationalizationTerm(i18n,text);
    }
  }
}

